public class ModelRumah {
    private Integer id, luasTanah, luasBangunan, kamarTidur, kamarMandi, garasi, price;
    private String alamat, status;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }

    
    
    public Integer getLuasTanah() {
        return luasTanah;
    }

    public void setLuasTanah(Integer luasTanah) {
        this.luasTanah = luasTanah;
    }

    public Integer getLuasBangunan() {
        return luasBangunan;
    }

    public void setLuasBangunan(Integer luasBangunan) {
        this.luasBangunan = luasBangunan;
    }

    public Integer getKamarTidur() {
        return kamarTidur;
    }

    public void setKamarTidur(Integer kamarTidur) {
        this.kamarTidur = kamarTidur;
    }

    public Integer getKamarMandi() {
        return kamarMandi;
    }

    public void setKamarMandi(Integer kamarMandi) {
        this.kamarMandi = kamarMandi;
    }

    public Integer getGarasi() {
        return garasi;
    }

    public void setGarasi(Integer garasi) {
        this.garasi = garasi;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
